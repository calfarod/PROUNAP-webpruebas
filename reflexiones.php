          <h2>3. Implementación y Aplicación de la Función de Investigación</h2>
          <p class="lead">
            La efectividad de la función de investigación no depende de su existencia, sino de una implementación
            estructurada, metódica y respaldada por la
            alta dirección. Un marco de gobernanza sólido y una metodología clara son esenciales para garantizar que las
            investigaciones sean legítimas,
            objetivas y generen valor. Esta sección detalla los principios rectores, las fases del proceso y los
            indicadores clave para medir su éxito.
          </p>
          <h3>3.1. Gobernanza y Principios Fundamentales</h3>
          <p class="lead">
            Para asegurar su legitimidad y eficacia, toda investigación interna debe regirse por principios
            fundamentales, como los delineados en la norma ISO 37008. Estos principios sostienen la credibilidad del
            proceso:
          </p>
          <ul class="lista">
            <li><span class="semi-bold">Legalidad y Legitimidad:</span> Asegurar el estricto cumplimiento de la
              normativa aplicable (laboral, de datos, penal) para garantizar la validez y defendibilidad de sus
              resultados.</li>
            <li><span class="semi-bold">Independencia:</span> La investigación no debe estar influenciada ni controlada
              por personas o áreas con intereses creados en el resultado, garantizando que el proceso no sea coartado.
            </li>
            <li><span class="semi-bold">Objetividad e Imparcialidad:</span> La investigación debe estar libre de
              conflictos de interés y basarse exclusivamente en evidencia fáctica, no en prejuicios o interpretaciones
              personales.</li>
            <li><span class="semi-bold">Competencia y Profesionalismo:</span> El proceso debe ser conducido por personal
              con el conocimiento, las habilidades y la experiencia adecuados para garantizar la calidad e integridad
              del trabajo.</li>
            <li><span class="semi-bold">Confidencialidad:</span> La información debe ser tratada con la máxima
              confidencialidad y revelada únicamente a quienes tengan una necesidad legítima de conocerla, protegiendo
              la integridad del proceso y de los involucrados.</li>
          </ul>
          <p class="lead">
            En la supervisión de este marco, el Comité de Auditoría juega un rol clave, con la responsabilidad de
            vigilar la efectividad del control interno y dirigir investigaciones especiales, asegurando así la
            independencia y el rigor necesarios.
          </p>
          <h3>3.2. Metodología y Fases de una Investigación Interna</h3>
          <p class="lead">
            Un proceso estructurado, como el descrito en la norma ISO 37008, garantiza la coherencia y la calidad de los
            resultados. Las fases clave son:
          </p>
          <ul class="lista">
            <li>1. Recepción y Análisis Preliminar: Evaluar la denuncia para determinar su credibilidad, identificar
              riesgos inmediatos y definir el alcance inicial de la investigación.</li>
            <li>2. Planificación del Proceso: Diseñar un plan de trabajo que establezca objetivos claros, recursos
              necesarios, metodologías a emplear y un cronograma.</li>
            <li>3. Recopilación y Documentación de Evidencia: Recabar de manera sistemática toda la evidencia relevante,
              asegurando su integridad y cadena de custodia.</li>
            <li>4. Realización de Entrevistas: Preparar y conducir entrevistas con las partes involucradas y testigos de
              manera profesional e imparcial para obtener información fáctica.</li>
            <li>5. Elaboración del Informe de Investigación: Documentar de manera clara, concisa y basada en hechos los
              hallazgos, conclusiones y recomendaciones.</li>
            <li>6. Medidas Posteriores: Aplicar las medidas disciplinarias y correctivas correspondientes, comunicar los
              resultados según los protocolos y
              utilizar los aprendizajes para mejorar los controles internos.</li>
          </ul>
          <h3>3.3. Medición del Desempeño a través de KPIs</h3>
          <p class="lead">
            Para justificar la inversión y asegurar la mejora continua, la función de investigación debe medirse con
            Indicadores Clave de Desempeño (KPIs) alineados con los objetivos estratégicos.
          </p>
          <img src="img/bsc.jpg" alt="Imagen 1" class="img-fluid mb-4 img1-seccion1" />
          <p class="">
            Imagen: El Balanced Scorecard haciendo un seguimiento estadístico de los KPIs del modelo de gestión de la
            calidad. BSC es una herramienta valiosa en la presentación de los resultados de la investigación.
          </p>
          <p class="lead">La correcta implementación de estos procesos y su medición sistemática subrayan el profundo
            valor estratégico que la función de investigación
            aporta a la organización.
          </p>
