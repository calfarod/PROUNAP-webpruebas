        if (
          !navPanelQuienes.contains(e.target) &&
          e.target !== navBtnQuienes
        ) {
          navPanelQuienes.classList.remove("show");
        }
      });

      navPanelQuienes.querySelectorAll("a.btn").forEach(function (btn) {
        btn.addEventListener("click", function () {
          navPanelQuienes.classList.remove("show");
        });
      });
    });
